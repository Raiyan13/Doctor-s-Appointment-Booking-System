    public int getEmployeeID(String employeeName) {
        DatabaseManager hospitalData = DatabaseManager.getInstace();
        int employeeID = 0;
        if (hospitalData.checkConnection()) {
            ResultSet rset = null;
            try {
                String query = "Select employeeID from tbl_employee where emp_name = " + "'" + employeeName + "';";
                rset = hospitalData.getResult(query);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Please Check Database.Something is Wrong!!", "Warning", JOptionPane.WARNING_MESSAGE);
                Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                while (rset.next()) {
                    employeeID = Integer.parseInt(rset.getString("employeeID"));
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Please Check Database.Something is Wrong!!", "Warning", JOptionPane.WARNING_MESSAGE);
                Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return employeeID;

    }

