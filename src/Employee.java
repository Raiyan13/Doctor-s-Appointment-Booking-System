    private void srcKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_srcKeyReleased
        ResultSet rs;
        DatabaseManager employeeData = DatabaseManager.getInstace();
        PreparedStatement pst;
        String search = src.getText();

        try {
            if (search.matches("^[0-9]+$") && search.charAt(0) != '0') {
                String query = "Select * from tbl_employee where employeeId = " + search + " or hospitalID = " + search;
                pst = employeeData.getPreparedStatement(query);
                rs = pst.executeQuery();
                employee.setModel(DbUtils.resultSetToTableModel(rs));
            } else {
                String query = "Select * from tbl_employee where emp_name Like '%" + search + "%' or emp_gender Like '" + search + "%' or emp_address Like '%" + search + "%' or emp_email Like '%" + search + "%' or emp_contact Like '%" + search + "%'";
                pst = employeeData.getPreparedStatement(query);
                rs = pst.executeQuery();
                employee.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please Check Database.Something is Wrong!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_srcKeyReleased

