    private void getEmployee() {

        Hospital hospital = new Hospital();
        hospitalComboBox.removeAllItems();
        hospital.setHospitalComboBox(this.hospitalComboBox);

        DefaultTableModel mod = (DefaultTableModel) employee.getModel();
        while (mod.getRowCount() > 0) {
            mod.removeRow(0);
        }

        DatabaseManager employeeData = DatabaseManager.getInstace();
        //System.out.println(patient.checkConnection());

        if (employeeData.checkConnection()) {
            String query = "Select * from tbl_employee";
            ResultSet rset = null;
            try {
                rset = employeeData.getResult(query);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Please Check Database.Something is Wrong!!", "Warning", JOptionPane.WARNING_MESSAGE);
                Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                while (rset.next()) {
                    int id = rset.getInt("employeeId");
                    String name = rset.getString("emp_name");
                    String gen = rset.getString("emp_gender");
                    String contact = rset.getString("emp_contact");
                    String email = rset.getString("emp_email");
                    String address = rset.getString("emp_address");
                    String hos = rset.getString("hospitalID");

                    Object[] row = {id, name, gen, contact, email, address, hos};

                    DefaultTableModel model = (DefaultTableModel) employee.getModel();
                    model.addRow(row);

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Please Check Database.Something is Wrong!!", "Warning", JOptionPane.WARNING_MESSAGE);
                Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void setEmployee() throws SQLException {

        Hospital hospital = new Hospital();
        String nam = name.getText();
        String ph = phone.getText();
        String email = mail.getText();
        String add = address.getText();
        String hsName = hospitalComboBox.getSelectedItem().toString();
        int hos = hospital.getHospitalID(hsName);

        String gen = null;

        if (male.isSelected()) {
            gen = "male";
        } else if (female.isSelected()) {
            gen = "female";
        }

        if (nam.isEmpty() || ph.isEmpty() || email.isEmpty() || gen.isEmpty() || hsName.isEmpty() || add.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill The Entry Form Correctly", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                DatabaseManager employeeData = DatabaseManager.getInstace();

                String query = "INSERT INTO tbl_employee(emp_name,emp_gender,emp_contact,emp_email,emp_address,hospitalId) VALUES(?,?,?,?,?,?)";
                PreparedStatement pst = employeeData.getPreparedStatement(query);

                pst.setString(1, nam);
                pst.setString(2, gen);
                pst.setString(3, ph);
                pst.setString(4, email);
                pst.setString(5, add);
                pst.setInt(6, hos);

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data Inserted Successfully!", "Connection Status", JOptionPane.INFORMATION_MESSAGE);

                getEmployee();

                clear();
            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, "Please Check The Connection Settings!\nOr Fill The From Correctly", "Error!", JOptionPane.ERROR_MESSAGE);

            }
        }
    }

